const generatedFileMarker = "Code generated by generator. DO NOT EDIT."

var errStopCleanupWalk = errors.New("stop cleanup walk")

type cleanupInventory struct {
	apiFiles                map[string]struct{}
	controllerFiles         map[string]struct{}
	registrationFiles       map[string]struct{}
	serviceManagerFiles     map[string]struct{}
	managerCmdFiles         map[string]struct{}
	managerConfigFiles      map[string]struct{}
	mutabilityOverlayFiles  map[string]struct{}
	packageGroups           map[string]struct{}
	sampleFiles             map[string]struct{}
	selectedGroups          map[string]struct{}
	selectedOverlayServices map[string]struct{}
	selectedSamplePrefixes  map[string]struct{}
	serviceManagerRoots     map[string]struct{}
}

func cleanupGeneratedOutputs(
	root string,
	services []ServiceConfig,
	packages []*PackageModel,
	mutabilityOverlayArtifacts []mutabilityOverlayGeneratedArtifact,
	fullSync bool,
) error {
	inventory, err := buildCleanupInventory(root, services, packages, mutabilityOverlayArtifacts, fullSync)
	if err != nil {
		return fmt.Errorf("build cleanup inventory: %w", err)
	}

	if err := cleanupAPIOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup api outputs: %w", err)
	}
	if err := cleanupAPICompanionOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup api companion outputs: %w", err)
	}
	if err := cleanupControllerOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup controller outputs: %w", err)
	}
	if err := cleanupRegistrationOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup registration outputs: %w", err)
	}
	if err := cleanupServiceManagerOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup service-manager outputs: %w", err)
	}
	if err := cleanupManagerCmdOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup manager cmd outputs: %w", err)
	}
	if err := cleanupManagerConfigOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup manager config outputs: %w", err)
	}
	if err := cleanupMutabilityOverlayOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup mutability overlay outputs: %w", err)
	}
	if err := cleanupPackageOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup package outputs: %w", err)
	}
	if err := cleanupSampleOutputs(root, inventory); err != nil {
		return fmt.Errorf("cleanup sample outputs: %w", err)
	}

	return nil
}

func buildCleanupInventory(
	root string,
	services []ServiceConfig,
	packages []*PackageModel,
	mutabilityOverlayArtifacts []mutabilityOverlayGeneratedArtifact,
	fullSync bool,
) (cleanupInventory, error) {
	inventory := newCleanupInventory(services)
	if fullSync {
		if err := inventory.includeExistingGeneratedCleanupScope(root); err != nil {
			return cleanupInventory{}, err
		}
	}

	for _, pkg := range packages {
		inventory.includePackageOutputs(root, pkg)
		splitPackages, err := buildPackageSplitModels(pkg)
		if err != nil {
			return cleanupInventory{}, err
		}
		for _, splitPkg := range splitPackages {
			inventory.includeSplitPackageOutputs(root, splitPkg)
		}
	}
	inventory.includeMutabilityOverlayOutputs(root, mutabilityOverlayArtifacts)

	return inventory, nil
}

func newCleanupInventory(services []ServiceConfig) cleanupInventory {
	inventory := cleanupInventory{
		apiFiles:                map[string]struct{}{},
		controllerFiles:         map[string]struct{}{},
		registrationFiles:       map[string]struct{}{},
		serviceManagerFiles:     map[string]struct{}{},
		managerCmdFiles:         map[string]struct{}{},
		managerConfigFiles:      map[string]struct{}{},
		mutabilityOverlayFiles:  map[string]struct{}{},
		packageGroups:           map[string]struct{}{},
		sampleFiles:             map[string]struct{}{},
		selectedGroups:          map[string]struct{}{},
		selectedOverlayServices: map[string]struct{}{},
		selectedSamplePrefixes:  map[string]struct{}{},
		serviceManagerRoots:     selectedServiceManagerRoots(services),
	}

	for _, service := range services {
		inventory.addCleanupGroup(service.Group)
		inventory.addOverlayService(service.Service)
	}
	return inventory
}

func (inventory *cleanupInventory) includePackageOutputs(root string, pkg *PackageModel) {
	apiDir := filepath.Join(root, "api", pkg.Service.Group, pkg.Version)
	inventory.addSelectedGroup(pkg.OutputName)
	inventory.apiFiles[filepath.Join(apiDir, "groupversion_info.go")] = struct{}{}
	inventory.includeResourceOutputs(root, apiDir, pkg.Resources)
	inventory.includeControllerOutputs(root, pkg)
	inventory.includeRegistrationOutputs(root, pkg)
	inventory.includeServiceManagerOutputs(root, pkg.ServiceManagers)
	inventory.includeManagerOutputs(root, pkg)
	if pkg.PackageOutput.Generate {
		inventory.packageGroups[pkg.OutputName] = struct{}{}
	}
}

func (inventory *cleanupInventory) includeSplitPackageOutputs(root string, pkg *PackageModel) {
	inventory.addSelectedGroup(pkg.OutputName)
	inventory.includeRegistrationOutputs(root, pkg)
	inventory.includeManagerOutputs(root, pkg)
	if pkg.PackageOutput.Generate {
		inventory.packageGroups[pkg.OutputName] = struct{}{}
	}
}

func (inventory *cleanupInventory) includeMutabilityOverlayOutputs(root string, artifacts []mutabilityOverlayGeneratedArtifact) {
	for _, artifact := range artifacts {
		inventory.addOverlayService(artifact.Service)
		inventory.mutabilityOverlayFiles[filepath.Join(root, filepath.FromSlash(artifact.RelativePath))] = struct{}{}
	}
}

func (inventory *cleanupInventory) includeResourceOutputs(root string, apiDir string, resources []ResourceModel) {
	for _, resource := range resources {
		inventory.apiFiles[filepath.Join(apiDir, resource.FileStem+"_types.go")] = struct{}{}
		if strings.TrimSpace(resource.Sample.FileName) != "" {
			inventory.sampleFiles[filepath.Join(root, "config", "samples", resource.Sample.FileName)] = struct{}{}
		}
	}
}

func (inventory *cleanupInventory) includeControllerOutputs(root string, pkg *PackageModel) {
	for _, controller := range pkg.Controller.Resources {
		inventory.controllerFiles[filepath.Join(root, "controllers", pkg.Service.Group, controller.FileStem+"_controller.go")] = struct{}{}
	}
}

func (inventory *cleanupInventory) includeRegistrationOutputs(root string, pkg *PackageModel) {
	if len(pkg.Registration.Resources) == 0 {
		return
	}
	inventory.registrationFiles[filepath.Join(root, "internal", "registrations", pkg.Registration.Group+"_generated.go")] = struct{}{}
}

func (inventory *cleanupInventory) includeServiceManagerOutputs(root string, serviceManagers []ServiceManagerModel) {
	for _, serviceManager := range serviceManagers {
		outputDir := filepath.Join(root, "pkg", "servicemanager", filepath.FromSlash(serviceManager.PackagePath))
		inventory.serviceManagerFiles[filepath.Join(outputDir, serviceManager.ServiceClientFileName)] = struct{}{}
		inventory.serviceManagerFiles[filepath.Join(outputDir, serviceManager.ServiceManagerFileName)] = struct{}{}
	}
}

func (inventory *cleanupInventory) includeManagerOutputs(root string, pkg *PackageModel) {
	if !pkg.Service.IsControllerBacked() {
		return
	}

	managerCmdDir := managerCmdOutputDir(root, pkg)
	inventory.managerCmdFiles[filepath.Join(managerCmdDir, "main.go")] = struct{}{}

	managerConfigDir := managerConfigOutputDir(root, pkg)
	for _, name := range []string{"kustomization.yaml", "manager.yaml", "controller_manager_config.yaml"} {
		inventory.managerConfigFiles[filepath.Join(managerConfigDir, name)] = struct{}{}
	}
}

func cleanupAPIOutputs(root string, inventory cleanupInventory) error {
	apiRoot := filepath.Join(root, "api")
	for _, group := range scopedGroupEntries(inventory.selectedGroups) {
		groupDir := filepath.Join(apiRoot, group)
		if err := deleteGeneratedFiles(groupDir, apiRoot, inventory.apiFiles, isOwnedAPIFile); err != nil {
			return err
		}
	}
	return nil
}

func cleanupAPICompanionOutputs(root string, inventory cleanupInventory) error {
	apiRoot := filepath.Join(root, "api")
	for _, group := range scopedGroupEntries(inventory.selectedGroups) {
		groupDir := filepath.Join(apiRoot, group)
		entries, err := os.ReadDir(groupDir)
		if os.IsNotExist(err) {
			continue
		}
		if err != nil {
			return err
		}

		for _, entry := range entries {
			if !entry.IsDir() {
				continue
			}
			versionDir := filepath.Join(groupDir, entry.Name())
			if versionDirHasDesiredAPIFiles(versionDir, inventory.apiFiles) {
				continue
			}

			deepcopyPath := filepath.Join(versionDir, "zz_generated.deepcopy.go")
			if err := os.Remove(deepcopyPath); err != nil && !os.IsNotExist(err) {
				return err
			}
			if err := pruneEmptyParents(versionDir, apiRoot); err != nil {
				return err
			}
		}
	}
	return nil
}

