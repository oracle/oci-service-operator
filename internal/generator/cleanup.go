func dirContainsOwnedFiles(root string, owned func(string) bool) (bool, error) {
	if _, err := os.Stat(root); os.IsNotExist(err) {
		return false, nil
	} else if err != nil {
		return false, err
	}

	err := filepath.WalkDir(root, func(path string, entry fs.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		if entry.IsDir() || !owned(entry.Name()) {
			return nil
		}
		return errStopCleanupWalk
	})
	if errors.Is(err, errStopCleanupWalk) {
		return true, nil
	}
	if err != nil {
		return false, err
	}
	return false, nil
}

func dirContainsGeneratedOwnedFiles(root string, owned func(string) bool) (bool, error) {
	if _, err := os.Stat(root); os.IsNotExist(err) {
		return false, nil
	} else if err != nil {
		return false, err
	}

	err := filepath.WalkDir(root, func(path string, entry fs.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		if entry.IsDir() || !owned(entry.Name()) {
			return nil
		}
		generated, err := fileContainsGeneratedMarker(path)
		if err != nil {
			return err
		}
		if generated {
			return errStopCleanupWalk
		}
		return nil
	})
	if errors.Is(err, errStopCleanupWalk) {
		return true, nil
	}
	if err != nil {
		return false, err
	}
	return false, nil
}

func fileContainsGeneratedMarker(path string) (bool, error) {
	content, err := os.ReadFile(path)
	if err != nil {
		return false, err
	}
	return strings.Contains(string(content), generatedFileMarker), nil
}

func isOwnedMutabilityOverlayArtifactFile(name string) bool {
	return filepath.Ext(name) == ".json"
}

func versionDirHasDesiredAPIFiles(versionDir string, desired map[string]struct{}) bool {
	for path := range desired {
		if filepath.Dir(path) == versionDir {
			return true
		}
	}
	return false
}

func generatedSampleGroup(name string) (string, bool) {
	base := strings.TrimSuffix(name, filepath.Ext(name))
	parts := strings.SplitN(base, "_", 3)
	if len(parts) != 3 {
		return "", false
	}
	group := strings.TrimSpace(parts[0])
	version := strings.TrimSpace(parts[1])
	fileStem := strings.TrimSpace(parts[2])
	if group == "" || fileStem == "" || !strings.HasPrefix(version, "v") {
		return "", false
	}
	return group, true
}

func pruneEmptyParents(dir string, stop string) error {
	dir = filepath.Clean(dir)
	stop = filepath.Clean(stop)
	for {
		if dir == stop || dir == "." || dir == string(filepath.Separator) {
			return nil
		}

		entries, err := os.ReadDir(dir)
		if os.IsNotExist(err) {
			dir = filepath.Dir(dir)
			continue
		}
		if err != nil {
			return err
		}
		if len(entries) != 0 {
			return nil
		}
		if err := os.Remove(dir); err != nil && !os.IsNotExist(err) {
			return err
		}
		dir = filepath.Dir(dir)
	}
}

func scopedGroupEntries(selected map[string]struct{}) []string {
	names := make([]string, 0, len(selected))
	for name := range selected {
		names = append(names, name)
	}
	sort.Strings(names)
	return names
}

func mapKeys(values map[string]struct{}) []string {
	keys := make([]string, 0, len(values))
	for key := range values {
		keys = append(keys, key)
	}
	sort.Strings(keys)
	return keys
}

func selectedServiceManagerRoots(services []ServiceConfig) map[string]struct{} {
	roots := make(map[string]struct{}, len(services))
	for _, service := range services {
		roots[service.Group] = struct{}{}
		for _, override := range service.Generation.Resources {
			packagePath := strings.TrimSpace(override.ServiceManager.PackagePath)
			if packagePath == "" {
				continue
			}
			root := strings.Split(filepath.ToSlash(packagePath), "/")[0]
			if strings.TrimSpace(root) == "" {
				continue
			}
			roots[root] = struct{}{}
		}
	}
	return roots
}

func isOwnedAPIFile(name string) bool {
	// Deepcopy companions are produced by controller-gen, not cmd/generator.
	return name == "groupversion_info.go" ||
		strings.HasSuffix(name, "_types.go")
}

func isOwnedControllerFile(name string) bool {
	return strings.HasSuffix(name, "_controller.go")
}

func isOwnedRegistrationFile(name string) bool {
	return strings.HasSuffix(name, "_generated.go")
}

func isOwnedServiceManagerFile(name string) bool {
	return strings.HasSuffix(name, "_serviceclient.go") ||
		strings.HasSuffix(name, "_servicemanager.go")
}

func isOwnedManagerCmdFile(name string) bool {
	return name == "main.go"
}

func isOwnedManagerConfigFile(name string) bool {
	return name == "kustomization.yaml" ||
		name == "manager.yaml" ||
		name == "controller_manager_config.yaml"
}
