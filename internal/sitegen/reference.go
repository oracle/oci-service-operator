func loadKindsForVersion(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, fmt.Errorf("read api version directory %q: %w", dir, err)
	}

	fset := token.NewFileSet()
	var kinds []string
	for _, entry := range entries {
		if entry.IsDir() || !strings.HasSuffix(entry.Name(), "_types.go") {
			continue
		}
		filePath := filepath.Join(dir, entry.Name())
		file, err := parser.ParseFile(fset, filePath, nil, parser.ParseComments)
		if err != nil {
			return nil, fmt.Errorf("parse api kinds from %q: %w", filePath, err)
		}
		for _, decl := range file.Decls {
			genDecl, ok := decl.(*ast.GenDecl)
			if !ok || genDecl.Tok != token.TYPE || genDecl.Doc == nil {
				continue
			}
			if !strings.Contains(genDecl.Doc.Text(), "+kubebuilder:object:root=true") {
				continue
			}
			for _, spec := range genDecl.Specs {
				typeSpec, ok := spec.(*ast.TypeSpec)
				if !ok {
					continue
				}
				structType, ok := typeSpec.Type.(*ast.StructType)
				if !ok {
					continue
				}
				if isKubebuilderListType(structType) {
					continue
				}
				kinds = append(kinds, typeSpec.Name.Name)
			}
		}
	}

	sort.Strings(kinds)
	return compactSortedStrings(kinds), nil
}

func apiVersionForGroup(group string, version string) string {
	return fmt.Sprintf("%s.oracle.com/%s", group, version)
}

func apiAnchor(group string, version string) string {
	return group + "-" + version
}

func kindKey(group string, kind string) string {
	return group + "/" + kind
}

func packageGroup(crdPaths string) string {
	trimmed := strings.TrimPrefix(strings.TrimSpace(crdPaths), "./api/")
	before, _, _ := strings.Cut(trimmed, "/")
	return before
}

func filteredKinds(kinds []string, csv string) []string {
	if strings.TrimSpace(csv) == "" {
		return append([]string{}, kinds...)
	}
	return splitKinds(csv)
}

func splitKinds(csv string) []string {
	parts := strings.Split(csv, ",")
	kinds := make([]string, 0, len(parts))
	for _, part := range parts {
		part = strings.TrimSpace(part)
		if part == "" {
			continue
		}
		kinds = append(kinds, part)
	}
	sort.Strings(kinds)
	return compactSortedStrings(kinds)
}

func cloneReleaseGroups(groups []ReleaseGroup) []ReleaseGroup {
	out := make([]ReleaseGroup, 0, len(groups))
	for _, group := range groups {
		kinds := append([]string{}, group.Kinds...)
		sort.Strings(kinds)
		out = append(out, ReleaseGroup{
			Group:   group.Group,
			Version: group.Version,
			Kinds:   compactSortedStrings(kinds),
		})
	}
	return out
}

func compactSortedStrings(values []string) []string {
	if len(values) == 0 {
		return nil
	}
	out := make([]string, 0, len(values))
	var previous string
	for _, value := range values {
		if value == previous {
			continue
		}
		out = append(out, value)
		previous = value
	}
	return out
}

func isKubebuilderListType(structType *ast.StructType) bool {
	for _, field := range structType.Fields.List {
		for _, name := range field.Names {
			if name.Name == "Items" {
				return true
			}
		}
	}
	return false
}

func docsLink(from string, to string) string {
	rel, err := filepath.Rel(filepath.Dir(filepath.FromSlash(from)), filepath.FromSlash(to))
	if err != nil {
		return to
	}
	return filepath.ToSlash(rel)
}

func markdownLink(label string, target string) string {
	return "[" + label + "](" + target + ")"
}

func sampleCell(from string, resource ReferenceResource) string {
	if resource.SampleOutputPath == "" {
		return "—"
	}
	return markdownLink("Sample", docsLink(from, resource.SampleOutputPath))
}

func summaryCell(summary string) string {
	if strings.TrimSpace(summary) == "" {
		return "—"
	}
	return escapeMarkdownCell(summary)
}

func displayReleaseVersion(version string) string {
	if strings.TrimSpace(version) == "" {
		return "Not yet released"
	}
	return version
}

func formatSupportStatus(status string) string {
	switch strings.ToLower(strings.TrimSpace(status)) {
	case "ga":
		return "GA"
	case "preview":
		return "Preview"
	default:
		return strings.TrimSpace(status)
	}
}

func backtickJoin(values []string) string {
	if len(values) == 0 {
		return "—"
	}
	parts := make([]string, 0, len(values))
	for _, value := range values {
		parts = append(parts, "`"+value+"`")
	}
	return strings.Join(parts, ", ")
}

func escapeMarkdownCell(value string) string {
	replacer := strings.NewReplacer("|", "\\|", "\n", "<br>")
	return replacer.Replace(value)
}
