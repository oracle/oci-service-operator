const generatedMarkdownHeader = "<!-- Code generated by cmd/sitegen api. DO NOT EDIT. -->"

// APIReferenceBuildOptions configures API reference generation from checked-in CRD schemas.
type APIReferenceBuildOptions struct {
	RepoRoot      string
	OutputDir     string
	SampleRepoURL string
	SampleRepoRef string
}

// GeneratedFile is one generated Markdown output relative to the configured output directory.
type GeneratedFile struct {
	RelativePath string
	Content      string
}

// APIReferenceSite contains the generated landing page plus all group/version pages.
type APIReferenceSite struct {
	LandingPage GeneratedFile
	Pages       []GeneratedFile
}

type groupVersionKey struct {
	Group   string
	Version string
}

type groupVersionKindKey struct {
	Group   string
	Version string
	Kind    string
}

type apiReferencePage struct {
	Group     string
	FullGroup string
	Version   string
	Packages  []packageExposure
	Resources []apiReferenceResource
}

type apiReferenceResource struct {
	Kind          string
	Plural        string
	Scope         string
	Summary       string
	Sample        *sampleReference
	Packages      []packageExposure
	SpecSection   *schemaSection
	StatusSection *schemaSection
}

type apiReferenceResourceModel struct {
	Group        string
	FullGroup    string
	Version      string
	Kind         string
	Plural       string
	Scope        string
	Description  string
	SpecSchema   *apiextensionsv1.JSONSchemaProps
	StatusSchema *apiextensionsv1.JSONSchemaProps
}

type packageExposure struct {
	Package       string
	DisplayName   string
	SupportStatus string
	LatestVersion string
	Resources     []string
}

type sampleReference struct {
	Path       string
	OutputPath string
	URL        string
}

type schemaSection struct {
	Anchor      string
	RootAnchor  string
	Title       string
	Description string
	Fields      []schemaField
	Nested      []*schemaSection
}

type schemaField struct {
	Name        string
	Anchor      string
	Description string
	Type        string
	Required    bool
	Default     string
	Enum        []string
}

type kindCopy struct {
	Summary     string
	Description string
}

type latestReleasedPackage struct {
	ManifestVersion string
	PublishedAt     time.Time
	Package         ReleasePackage
}

// BuildAPIReferenceSite renders the API reference site in memory.
func BuildAPIReferenceSite(opts APIReferenceBuildOptions) (*APIReferenceSite, error) {
	opts, err := normalizeAPIReferenceOptions(opts)
	if err != nil {
		return nil, err
	}

	pages, err := loadAPIReferencePages(opts)
	if err != nil {
		return nil, err
	}

	renderedPages := make([]GeneratedFile, 0, len(pages))
	for _, page := range pages {
		renderedPages = append(renderedPages, GeneratedFile{
			RelativePath: path.Join(page.Group, page.Version, "index.md"),
			Content:      renderAPIReferencePage(page),
		})
	}

	return &APIReferenceSite{
		LandingPage: GeneratedFile{
			RelativePath: "index.md",
			Content:      renderAPIReferenceLanding(pages),
		},
		Pages: renderedPages,
	}, nil
}

func loadAPIReferencePages(opts APIReferenceBuildOptions) ([]*apiReferencePage, error) {
	var err error
	opts, err = normalizeAPIReferenceOptions(opts)
	if err != nil {
		return nil, err
	}

	catalog, err := LoadCatalog(filepath.Join(opts.RepoRoot, "docs", "site-data", "catalog.yaml"))
	if err != nil {
		return nil, err
	}
	releases, err := LoadReleaseManifests(filepath.Join(opts.RepoRoot, "docs", "site-data", "releases"))
	if err != nil {
		return nil, err
	}
	kindCopies, err := buildKindCopyIndex(catalog)
	if err != nil {
		return nil, err
	}
	pagePackages, kindPackages := buildReleaseExposureIndexes(catalog, releases)

	resources, err := loadAPIReferenceResources(filepath.Join(opts.RepoRoot, "config", "crd", "bases"))
	if err != nil {
		return nil, err
	}

	pagesByKey := make(map[groupVersionKey]*apiReferencePage)
	for _, resource := range resources {
		pageKey := groupVersionKey{Group: resource.Group, Version: resource.Version}
		page, exists := pagesByKey[pageKey]
		if !exists {
			page = &apiReferencePage{
				Group:     resource.Group,
				FullGroup: resource.FullGroup,
				Version:   resource.Version,
				Packages:  pagePackages[pageKey],
			}
			pagesByKey[pageKey] = page
		}

		resourceKey := groupVersionKindKey{
			Group:   resource.Group,
			Version: resource.Version,
			Kind:    resource.Kind,
		}
		page.Resources = append(page.Resources, buildAPIReferenceResourceDoc(opts, resource, kindCopies[resource.Group+"/"+resource.Kind], kindPackages[resourceKey]))
	}

	return sortAPIReferencePages(pagesByKey), nil
}

// GenerateAPIReferenceSite writes the API reference landing page plus group/version pages to disk.
func GenerateAPIReferenceSite(opts APIReferenceBuildOptions) error {
	opts, err := normalizeAPIReferenceOptions(opts)
	if err != nil {
		return err
	}

	site, err := BuildAPIReferenceSite(opts)
	if err != nil {
		return err
	}

	files := make([]GeneratedFile, 0, 1+len(site.Pages))
	files = append(files, site.LandingPage)
	files = append(files, site.Pages...)

	for _, file := range files {
		targetPath := filepath.Join(opts.OutputDir, filepath.FromSlash(file.RelativePath))
		if err := os.MkdirAll(filepath.Dir(targetPath), 0o755); err != nil {
			return fmt.Errorf("create directory for %q: %w", targetPath, err)
		}
		if err := os.WriteFile(targetPath, []byte(file.Content), 0o644); err != nil {
			return fmt.Errorf("write API reference file %q: %w", targetPath, err)
		}
	}

	return nil
}

func normalizeAPIReferenceOptions(opts APIReferenceBuildOptions) (APIReferenceBuildOptions, error) {
	repoRoot := strings.TrimSpace(opts.RepoRoot)
	if repoRoot == "" {
		return APIReferenceBuildOptions{}, fmt.Errorf("repo root is required")
	}
	opts.RepoRoot = filepath.Clean(repoRoot)

	if strings.TrimSpace(opts.OutputDir) == "" {
		opts.OutputDir = filepath.Join(opts.RepoRoot, "docs", "reference", "api")
	} else {
		opts.OutputDir = filepath.Clean(opts.OutputDir)
	}

	opts.SampleRepoURL = strings.TrimSuffix(strings.TrimSpace(opts.SampleRepoURL), "/")
	opts.SampleRepoRef = strings.TrimSpace(opts.SampleRepoRef)
	if opts.SampleRepoRef == "" {
		opts.SampleRepoRef = "main"
	}

	return opts, nil
}

func buildKindCopyIndex(catalog *Catalog) (map[string]kindCopy, error) {
	out := make(map[string]kindCopy)
	for _, pkg := range catalog.Packages {
		for _, override := range pkg.KindOverrides {
			key := override.Group + "/" + override.Kind
			current := out[key]
			if err := mergeKindCopy(&current, override); err != nil {
				return nil, fmt.Errorf("catalog package %q: %w", pkg.Package, err)
			}
			out[key] = current
		}
	}
	return out, nil
}

func mergeKindCopy(dst *kindCopy, override KindOverride) error {
	if summary := strings.TrimSpace(override.Summary); summary != "" {
		if dst.Summary != "" && dst.Summary != summary {
			return fmt.Errorf("conflicting summary override for %s/%s", override.Group, override.Kind)
		}
		dst.Summary = summary
	}
	if description := strings.TrimSpace(override.Description); description != "" {
		if dst.Description != "" && dst.Description != description {
			return fmt.Errorf("conflicting description override for %s/%s", override.Group, override.Kind)
		}
		dst.Description = description
	}
	return nil
}

func buildReleaseExposureIndexes(
	catalog *Catalog,
	releases []*ReleaseManifest,
) (map[groupVersionKey][]packageExposure, map[groupVersionKindKey][]packageExposure) {
	catalogByPackage := make(map[string]CatalogPackage, len(catalog.Packages))
	for _, pkg := range catalog.Packages {
		catalogByPackage[pkg.Package] = pkg
	}

	latestByPackage := latestReleasedPackages(releases)
	pageIndex := make(map[groupVersionKey]map[string]*packageExposure)
	kindIndex := make(map[groupVersionKindKey]map[string]*packageExposure)

	for packageName, latest := range latestByPackage {
		exposure := packageExposure{
			Package:       packageName,
			DisplayName:   packageName,
			SupportStatus: "-",
			LatestVersion: latest.ManifestVersion,
		}
		if catalogEntry, ok := catalogByPackage[packageName]; ok {
			exposure.DisplayName = catalogEntry.DisplayName
			exposure.SupportStatus = catalogEntry.SupportStatus
		}

		for _, group := range latest.Package.Groups {
			pageKey := groupVersionKey{Group: group.Group, Version: group.Version}
			pageExposure := ensurePageExposure(pageIndex, pageKey, exposure)
			pageExposure.Resources = append(pageExposure.Resources, group.Kinds...)

			for _, kind := range group.Kinds {
				kindKey := groupVersionKindKey{
					Group:   group.Group,
					Version: group.Version,
					Kind:    kind,
				}
				ensureKindExposure(kindIndex, kindKey, exposure)
			}
		}
	}

	return flattenPageExposureIndex(pageIndex), flattenKindExposureIndex(kindIndex)
}

func latestReleasedPackages(releases []*ReleaseManifest) map[string]latestReleasedPackage {
	out := make(map[string]latestReleasedPackage)
	for _, manifest := range releases {
		for _, releasedPackage := range manifest.Packages {
			current, exists := out[releasedPackage.Package]
			if exists {
				if current.PublishedAt.After(manifest.PublishedAt) {
					continue
				}
				if current.PublishedAt.Equal(manifest.PublishedAt) && current.ManifestVersion >= manifest.Version {
					continue
				}
			}
			packageCopy := releasedPackage
			out[releasedPackage.Package] = latestReleasedPackage{
				ManifestVersion: manifest.Version,
				PublishedAt:     manifest.PublishedAt,
				Package:         packageCopy,
			}
		}
	}
	return out
}

func ensurePageExposure(
	index map[groupVersionKey]map[string]*packageExposure,
	key groupVersionKey,
	base packageExposure,
) *packageExposure {
	if _, ok := index[key]; !ok {
		index[key] = make(map[string]*packageExposure)
	}
	if _, ok := index[key][base.Package]; !ok {
		exposureCopy := base
		index[key][base.Package] = &exposureCopy
	}
	return index[key][base.Package]
}

func ensureKindExposure(
	index map[groupVersionKindKey]map[string]*packageExposure,
	key groupVersionKindKey,
	base packageExposure,
) {
	if _, ok := index[key]; !ok {
		index[key] = make(map[string]*packageExposure)
	}
	if _, ok := index[key][base.Package]; ok {
		return
	}
	exposureCopy := base
	index[key][base.Package] = &exposureCopy
}

func flattenPageExposureIndex(index map[groupVersionKey]map[string]*packageExposure) map[groupVersionKey][]packageExposure {
	out := make(map[groupVersionKey][]packageExposure, len(index))
	for key, packagesByName := range index {
		exposures := make([]packageExposure, 0, len(packagesByName))
		for _, exposure := range packagesByName {
			copyExposure := *exposure
			copyExposure.Resources = compactSortedUniqueStrings(copyExposure.Resources)
			exposures = append(exposures, copyExposure)
		}
		sortPackageExposures(exposures)
		out[key] = exposures
	}
	return out
}

func flattenKindExposureIndex(index map[groupVersionKindKey]map[string]*packageExposure) map[groupVersionKindKey][]packageExposure {
	out := make(map[groupVersionKindKey][]packageExposure, len(index))
	for key, packagesByName := range index {
		exposures := make([]packageExposure, 0, len(packagesByName))
		for _, exposure := range packagesByName {
			exposures = append(exposures, *exposure)
		}
		sortPackageExposures(exposures)
		out[key] = exposures
	}
	return out
}

func sortPackageExposures(exposures []packageExposure) {
	sort.Slice(exposures, func(i, j int) bool {
		left := packageDisplayName(exposures[i])
		right := packageDisplayName(exposures[j])
		if left == right {
			return exposures[i].Package < exposures[j].Package
		}
		return left < right
	})
}

func loadAPIReferenceResources(dir string) ([]apiReferenceResourceModel, error) {
	pattern := filepath.Join(dir, "*.yaml")
	paths, err := filepath.Glob(pattern)
	if err != nil {
		return nil, fmt.Errorf("glob CRD schemas %q: %w", pattern, err)
	}
	sort.Strings(paths)

	var resources []apiReferenceResourceModel
	for _, crdPath := range paths {
		content, err := os.ReadFile(crdPath)
		if err != nil {
			return nil, fmt.Errorf("read CRD schema %q: %w", crdPath, err)
		}

		var crd apiextensionsv1.CustomResourceDefinition
		if err := yaml.UnmarshalStrict(content, &crd); err != nil {
			return nil, fmt.Errorf("decode CRD schema %q: %w", crdPath, err)
		}

		group := shortGroupName(crd.Spec.Group)
		for _, version := range crd.Spec.Versions {
			if !version.Served {
				continue
			}
			if version.Schema == nil || version.Schema.OpenAPIV3Schema == nil {
				return nil, fmt.Errorf("CRD schema %q version %q is missing openAPIV3Schema", crdPath, version.Name)
			}

			specSchema, _ := schemaProperty(version.Schema.OpenAPIV3Schema, "spec")
			statusSchema, _ := schemaProperty(version.Schema.OpenAPIV3Schema, "status")

			resources = append(resources, apiReferenceResourceModel{
				Group:        group,
				FullGroup:    crd.Spec.Group,
				Version:      version.Name,
				Kind:         crd.Spec.Names.Kind,
				Plural:       crd.Spec.Names.Plural,
				Scope:        string(crd.Spec.Scope),
				Description:  normalizeText(version.Schema.OpenAPIV3Schema.Description),
				SpecSchema:   specSchema,
				StatusSchema: statusSchema,
			})
		}
	}

	sort.Slice(resources, func(i, j int) bool {
		left := resources[i]
		right := resources[j]
		switch {
		case left.Group != right.Group:
			return left.Group < right.Group
		case left.Version != right.Version:
			return left.Version < right.Version
		default:
			return left.Kind < right.Kind
		}
	})

	return resources, nil
}

func shortGroupName(fullGroup string) string {
	group := strings.TrimSpace(fullGroup)
	if before, _, found := strings.Cut(group, "."); found && before != "" {
		return before
	}
	return group
}

func schemaProperty(root *apiextensionsv1.JSONSchemaProps, name string) (*apiextensionsv1.JSONSchemaProps, bool) {
	if root == nil {
		return nil, false
	}
	property, ok := root.Properties[name]
	if !ok {
		return nil, false
	}
	propertyCopy := property
	return &propertyCopy, true
}

func buildAPIReferenceResourceDoc(
	opts APIReferenceBuildOptions,
	model apiReferenceResourceModel,
	copy kindCopy,
	packages []packageExposure,
) apiReferenceResource {
	return apiReferenceResource{
		Kind:          model.Kind,
		Plural:        model.Plural,
		Scope:         model.Scope,
		Summary:       resolveKindSummary(model.Description, copy),
		Sample:        buildSampleReference(opts, model.Group, model.Version, model.Kind),
		Packages:      packages,
		SpecSection:   buildSchemaSection(model.Kind, "Spec", "spec", nil, model.SpecSchema),
		StatusSection: buildSchemaSection(model.Kind, "Status", "status", nil, model.StatusSchema),
	}
}

func resolveKindSummary(fallback string, copy kindCopy) string {
	switch {
	case strings.TrimSpace(copy.Description) != "":
		return normalizeText(copy.Description)
	case strings.TrimSpace(copy.Summary) != "":
		return normalizeText(copy.Summary)
	default:
		return normalizeText(fallback)
	}
}

func buildSampleReference(opts APIReferenceBuildOptions, group string, version string, kind string) *sampleReference {
	filename := fmt.Sprintf("%s_%s_%s.yaml", group, version, strings.ToLower(kind))
	relativePath := filepath.ToSlash(filepath.Join("config", "samples", filename))
	absolutePath := filepath.Join(opts.RepoRoot, filepath.FromSlash(relativePath))
	if _, err := os.Stat(absolutePath); err != nil {
		return nil
	}

	ref := &sampleReference{Path: relativePath}
	ref.OutputPath = filepath.ToSlash(filepath.Join("docs", "reference", "samples", group, version, strings.ToLower(kind)+".md"))
	if opts.SampleRepoURL != "" {
		ref.URL = opts.SampleRepoURL + "/" + path.Join("blob", opts.SampleRepoRef, relativePath)
	}
	return ref
}

func buildSchemaSection(
	kind string,
	rootLabel string,
	rootSegment string,
	pathSegments []string,
	schema *apiextensionsv1.JSONSchemaProps,
) *schemaSection {
	if schema == nil {
		return nil
	}

	rootAnchor := kindRootSectionAnchor(kind, rootSegment)
	section := &schemaSection{
		Anchor:      rootAnchor,
		RootAnchor:  rootAnchor,
		Title:       rootLabel,
		Description: normalizeText(schema.Description),
	}
	if len(pathSegments) > 0 {
		section.Anchor = schemaSectionAnchor(kind, rootSegment, pathSegments)
		section.Title = rootLabel + "." + strings.Join(pathSegments, ".")
	}

	required := makeRequiredSet(schema.Required)
	for _, fieldName := range sortedPropertyNames(schema.Properties) {
		property := schema.Properties[fieldName]
		field := schemaField{
			Name:        fieldName,
			Description: schemaFieldDescription(&property),
			Type:        schemaTypeLabel(&property),
			Required:    required[fieldName],
			Default:     schemaDefaultValue(&property),
			Enum:        schemaEnumValues(&property),
		}

		childSchema, childDisplay := nestedSchemaForProperty(fieldName, &property)
		if childSchema != nil {
			childSegments := append(append([]string{}, pathSegments...), childDisplay)
			field.Anchor = schemaSectionAnchor(kind, rootSegment, childSegments)
			section.Nested = append(section.Nested, buildSchemaSection(kind, rootLabel, rootSegment, childSegments, childSchema))
		}

		section.Fields = append(section.Fields, field)
	}

	return section
}

func makeRequiredSet(required []string) map[string]bool {
	out := make(map[string]bool, len(required))
	for _, fieldName := range required {
		out[fieldName] = true
	}
	return out
}

func sortedPropertyNames(properties map[string]apiextensionsv1.JSONSchemaProps) []string {
	names := make([]string, 0, len(properties))
	for name := range properties {
		names = append(names, name)
	}
	sort.Strings(names)
	return names
}

func nestedSchemaForProperty(fieldName string, schema *apiextensionsv1.JSONSchemaProps) (*apiextensionsv1.JSONSchemaProps, string) {
	if schema == nil {
		return nil, ""
	}

	switch schema.Type {
	case "array":
		if schema.Items != nil && schema.Items.Schema != nil && hasRenderableProperties(schema.Items.Schema) {
			return schema.Items.Schema, fieldName + "[]"
		}
	case "object":
		if len(schema.Properties) > 0 {
			return schema, fieldName
		}
		if schema.AdditionalProperties != nil && schema.AdditionalProperties.Schema != nil && hasRenderableProperties(schema.AdditionalProperties.Schema) {
			return schema.AdditionalProperties.Schema, fieldName + "{}"
		}
	}

	return nil, ""
}

func hasRenderableProperties(schema *apiextensionsv1.JSONSchemaProps) bool {
	if schema == nil {
		return false
	}
	if len(schema.Properties) > 0 {
		return true
	}
	if schema.Type == "array" && schema.Items != nil && schema.Items.Schema != nil {
		return hasRenderableProperties(schema.Items.Schema)
	}
	if schema.AdditionalProperties != nil && schema.AdditionalProperties.Schema != nil {
		return hasRenderableProperties(schema.AdditionalProperties.Schema)
	}
	return false
}

func schemaFieldDescription(schema *apiextensionsv1.JSONSchemaProps) string {
	parts := make([]string, 0, 2)
	if description := normalizeText(schema.Description); description != "" {
		parts = append(parts, description)
	}
	if notes := schemaSemanticsNotes(schema); notes != "" {
		parts = append(parts, notes)
	}
	return strings.Join(parts, " ")
}

func schemaSemanticsNotes(schema *apiextensionsv1.JSONSchemaProps) string {
	var notes []string

	if schema.XListType != nil && strings.TrimSpace(*schema.XListType) != "" {
		note := "List semantics: " + strings.TrimSpace(*schema.XListType)
		if strings.EqualFold(strings.TrimSpace(*schema.XListType), "map") && len(schema.XListMapKeys) > 0 {
			note += " keyed by `" + strings.Join(schema.XListMapKeys, "`, `") + "`"
		}
		notes = append(notes, note+".")
	}

	if schema.XMapType != nil && strings.TrimSpace(*schema.XMapType) != "" {
		notes = append(notes, "Map semantics: "+strings.TrimSpace(*schema.XMapType)+".")
	}

	if schema.XPreserveUnknownFields != nil && *schema.XPreserveUnknownFields {
		notes = append(notes, "Unknown nested fields are preserved.")
	}

	validationMessages := validationRuleMessages(schema.XValidations)
	if len(validationMessages) > 0 {
		notes = append(notes, "Validation: "+strings.Join(validationMessages, "; ")+".")
	}

	return strings.Join(notes, " ")
}

func validationRuleMessages(rules apiextensionsv1.ValidationRules) []string {
	messages := make([]string, 0, len(rules))
	for _, rule := range rules {
		message := normalizeText(rule.Message)
		if message == "" {
			continue
		}
		messages = append(messages, message)
	}
	return compactSortedUniqueStrings(messages)
}

func schemaTypeLabel(schema *apiextensionsv1.JSONSchemaProps) string {
	if schema == nil {
		return "object"
	}
	if schema.XIntOrString {
		return "int or string"
	}

	switch schema.Type {
	case "array":
		itemType := "object"
		if schema.Items != nil && schema.Items.Schema != nil {
			itemType = schemaTypeLabel(schema.Items.Schema)
		}
		return fmt.Sprintf("list[%s]", itemType)
	case "object":
		if schema.AdditionalProperties != nil && schema.AdditionalProperties.Schema != nil {
			return fmt.Sprintf("map[string, %s]", schemaTypeLabel(schema.AdditionalProperties.Schema))
		}
		if schema.XPreserveUnknownFields != nil && *schema.XPreserveUnknownFields {
			return "object (preserves unknown fields)"
		}
		return "object"
	case "":
		if len(schema.AnyOf) > 0 || len(schema.OneOf) > 0 || len(schema.AllOf) > 0 {
			return "union"
		}
		return "object"
	default:
		if strings.TrimSpace(schema.Format) == "" {
			return schema.Type
		}
		return fmt.Sprintf("%s (%s)", schema.Type, schema.Format)
	}
}

func schemaDefaultValue(schema *apiextensionsv1.JSONSchemaProps) string {
	if schema == nil || schema.Default == nil || len(schema.Default.Raw) == 0 {
		return ""
	}
	return humanJSONValue(schema.Default.Raw)
}

func schemaEnumValues(schema *apiextensionsv1.JSONSchemaProps) []string {
	if schema == nil || len(schema.Enum) == 0 {
		return nil
	}
	values := make([]string, 0, len(schema.Enum))
	for _, raw := range schema.Enum {
		values = append(values, humanJSONValue(raw.Raw))
	}
	return compactSortedUniqueStrings(values)
}

func humanJSONValue(raw []byte) string {
	var value any
	if err := json.Unmarshal(raw, &value); err != nil {
		return strings.TrimSpace(string(raw))
	}
	return humanValue(value)
}

func humanValue(value any) string {
	switch typed := value.(type) {
	case nil:
		return "null"
	case string:
		return typed
	case bool:
		if typed {
			return "true"
		}
		return "false"
	case float64:
		if typed == float64(int64(typed)) {
			return fmt.Sprintf("%d", int64(typed))
		}
		return fmt.Sprintf("%g", typed)
	default:
		encoded, err := json.Marshal(typed)
		if err != nil {
			return fmt.Sprintf("%v", typed)
		}
		return string(encoded)
	}
}

func normalizeText(text string) string {
	return strings.Join(strings.Fields(strings.TrimSpace(text)), " ")
}

func compactSortedUniqueStrings(values []string) []string {
	if len(values) == 0 {
		return nil
	}
	sort.Strings(values)
	out := values[:0]
	for _, value := range values {
		trimmed := strings.TrimSpace(value)
		if trimmed == "" {
			continue
		}
		if len(out) > 0 && out[len(out)-1] == trimmed {
			continue
		}
		out = append(out, trimmed)
	}
	return out
}

func sortAPIReferencePages(pagesByKey map[groupVersionKey]*apiReferencePage) []*apiReferencePage {
	pages := make([]*apiReferencePage, 0, len(pagesByKey))
	for _, page := range pagesByKey {
		sort.Slice(page.Resources, func(i, j int) bool {
			return page.Resources[i].Kind < page.Resources[j].Kind
		})
		pages = append(pages, page)
	}

	sort.Slice(pages, func(i, j int) bool {
		switch {
		case pages[i].Group != pages[j].Group:
			return pages[i].Group < pages[j].Group
		default:
			return pages[i].Version < pages[j].Version
		}
	})
	return pages
}

