func renderAPIReferencePage(page *apiReferencePage) string {
	outputPath := filepath.ToSlash(filepath.Join("docs", "reference", "api", page.Group, page.Version, "index.md"))

	var b strings.Builder
	b.WriteString(generatedMarkdownHeader)
	b.WriteString("\n\n")
	fmt.Fprintf(&b, "# %s/%s\n\n", page.FullGroup, page.Version)
	b.WriteString("[Back to API Reference](../../index.md)\n\n")
	fmt.Fprintf(&b, "`APIVersion`: `%s/%s`\n\n", page.FullGroup, page.Version)
	b.WriteString(generatorOwnershipNote())
	b.WriteString("\n\n")

	b.WriteString("<a id=\"packages\"></a>\n## Packages\n\n")
	if len(page.Packages) == 0 {
		fmt.Fprintf(&b, "No released package currently exposes `%s/%s`.\n\n", page.FullGroup, page.Version)
	} else {
		b.WriteString("| Package | Support | Latest release | Resources |\n")
		b.WriteString("| --- | --- | --- | --- |\n")
		for _, pkg := range page.Packages {
			fmt.Fprintf(
				&b,
				"| %s | %s | %s | %s |\n",
				escapeTableText(packageDisplayName(pkg)),
				escapeTableText(dashIfEmpty(pkg.SupportStatus)),
				codeSpan(pkg.LatestVersion),
				renderPackageResourceLinks(pkg.Resources),
			)
		}
		b.WriteString("\n")
	}

	b.WriteString("<a id=\"resources\"></a>\n## Resources\n\n")
	b.WriteString("| Kind | Scope | Sample | Released packages |\n")
	b.WriteString("| --- | --- | --- | --- |\n")
	for _, resource := range page.Resources {
		fmt.Fprintf(
			&b,
			"| %s | %s | %s | %s |\n",
			fmt.Sprintf("[%s](#%s)", resource.Kind, kindAnchor(resource.Kind)),
			escapeTableText(resource.Scope),
			renderSampleCell(outputPath, resource.Sample),
			renderResourcePackageCell(resource.Packages),
		)
	}
	b.WriteString("\n")

	for _, resource := range page.Resources {
		renderAPIReferenceResource(&b, page, outputPath, resource)
	}

	return b.String()
}

func renderAPIReferenceResource(b *strings.Builder, page *apiReferencePage, outputPath string, resource apiReferenceResource) {
	fmt.Fprintf(b, "<a id=\"%s\"></a>\n## %s\n\n", kindAnchor(resource.Kind), resource.Kind)
	if resource.Summary != "" {
		b.WriteString(resource.Summary)
		b.WriteString("\n\n")
	}
	fmt.Fprintf(b, "- `Plural`: %s\n", codeSpan(resource.Plural))
	fmt.Fprintf(b, "- `Scope`: %s\n", codeSpan(resource.Scope))
	fmt.Fprintf(b, "- `APIVersion`: %s\n", codeSpan(page.FullGroup+"/"+page.Version))
	fmt.Fprintf(b, "- `Sample`: %s\n", renderSampleBullet(outputPath, resource.Sample))
	fmt.Fprintf(b, "- `Released packages`: %s\n\n", renderResourcePackageBullet(resource.Packages))

	renderSchemaSection(b, resource.Kind, "Spec", resource.SpecSection, 0)
	renderSchemaSection(b, resource.Kind, "Status", resource.StatusSection, 0)
}

func renderSchemaSection(
	b *strings.Builder,
	kind string,
	rootLabel string,
	section *schemaSection,
	depth int,
) {
	if section == nil {
		return
	}

	level := 3 + depth
	if level > 6 {
		level = 6
	}
	fmt.Fprintf(b, "<a id=\"%s\"></a>\n%s %s\n\n", section.Anchor, strings.Repeat("#", level), section.Title)
	if depth > 0 {
		fmt.Fprintf(b, "[Back to %s %s](#%s)\n\n", kind, strings.ToLower(rootLabel), section.RootAnchor)
	}
	if section.Description != "" {
		b.WriteString(section.Description)
		b.WriteString("\n\n")
	}

	if len(section.Fields) == 0 {
		b.WriteString("No documented fields in the checked-in CRD schema.\n\n")
	} else {
		b.WriteString("| Field | Description | Type | Required | Default | Enum |\n")
		b.WriteString("| --- | --- | --- | --- | --- | --- |\n")
		for _, field := range section.Fields {
			fmt.Fprintf(
				b,
				"| %s | %s | %s | %s | %s | %s |\n",
				renderFieldNameCell(field),
				escapeTableText(dashIfEmpty(field.Description)),
				codeSpan(field.Type),
				boolLabel(field.Required),
				renderOptionalCodeCell(field.Default),
				renderEnumCell(field.Enum),
			)
		}
		b.WriteString("\n")
	}

	for _, nested := range section.Nested {
		renderSchemaSection(b, kind, rootLabel, nested, depth+1)
	}
}

