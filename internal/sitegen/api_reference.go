func renderSampleBullet(from string, sample *sampleReference) string {
	if sample == nil {
		return "No checked-in sample manifest currently exists."
	}
	link := renderSampleMarkdownLink(from, sample)
	if sample.Path == "" {
		return link
	}
	return link + " (" + codeSpan(sample.Path) + ")"
}

func renderSampleMarkdownLink(from string, sample *sampleReference) string {
	if sample.OutputPath != "" {
		return markdownLink("Sample", docsLink(from, sample.OutputPath))
	}
	label := path.Base(sample.Path)
	if sample.URL != "" {
		return markdownLink(label, sample.URL)
	}
	return codeSpan(sample.Path)
}

func renderFieldNameCell(field schemaField) string {
	if field.Anchor == "" {
		return codeSpan(field.Name)
	}
	return fmt.Sprintf("[%s](#%s)", codeSpan(field.Name), field.Anchor)
}

func renderOptionalCodeCell(value string) string {
	if strings.TrimSpace(value) == "" {
		return "-"
	}
	return codeSpan(value)
}

func renderEnumCell(values []string) string {
	if len(values) == 0 {
		return "-"
	}
	formatted := make([]string, 0, len(values))
	for _, value := range values {
		formatted = append(formatted, codeSpan(value))
	}
	return strings.Join(formatted, ", ")
}

func boolLabel(required bool) string {
	if required {
		return "Yes"
	}
	return "No"
}

func dashIfEmpty(value string) string {
	if strings.TrimSpace(value) == "" {
		return "-"
	}
	return value
}

func escapeTableText(value string) string {
	normalized := strings.TrimSpace(value)
	normalized = strings.ReplaceAll(normalized, "\n", "<br>")
	normalized = strings.ReplaceAll(normalized, "|", "\\|")
	return normalized
}

func packageDisplayName(exposure packageExposure) string {
	if strings.TrimSpace(exposure.DisplayName) != "" {
		return exposure.DisplayName
	}
	return exposure.Package
}

func kindAnchor(kind string) string {
	return "kind-" + slug(kind)
}

func kindRootSectionAnchor(kind string, rootSegment string) string {
	return strings.Join([]string{"kind", slug(kind), slug(rootSegment)}, "-")
}

func schemaSectionAnchor(kind string, rootSegment string, pathSegments []string) string {
	parts := []string{"kind", slug(kind), slug(rootSegment)}
	for _, segment := range pathSegments {
		parts = append(parts, slug(segment))
	}
	return strings.Join(parts, "-")
}

func slug(value string) string {
	var b strings.Builder
	lastDash := false
	for _, r := range strings.ToLower(value) {
		switch {
		case r >= 'a' && r <= 'z':
			b.WriteRune(r)
			lastDash = false
		case r >= '0' && r <= '9':
			b.WriteRune(r)
			lastDash = false
		default:
			if b.Len() == 0 || lastDash {
				continue
			}
			b.WriteRune('-')
			lastDash = true
		}
	}
	return strings.Trim(b.String(), "-")
}

func codeSpan(value string) string {
	return "`" + value + "`"
}
